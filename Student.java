public class Student {
    
    private String name , grade , id,gender,faculty;

    public void setName(String setname){
        this.name = setname;
    }

    public String getName(){
        return name;
    }

    public void setGrade(String setgrade){
        this.grade = setgrade;
    }

    public String getGrade(){
        return grade;
    }

    public void setId(String setid){
        this.id = setid;
    }

    public String getId(){
        return id;
    }

    public void setGender(String setgender){
        this.gender = setgender;
    }

    public String getGender(){
        return gender;
    }

    public void setFaculty(String setfaculty){
        this.faculty = setfaculty;
    }

    public String getFaculty(){
        return faculty;
    }
}